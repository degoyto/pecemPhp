        <h1>O JORNAL PORTO DO PECÉM</h1>
        <hr/>
        <p>
            Informar, reportar e opinar sobre todos os quadrantes da problemática econômica do Ceará, do Brasil e do mundo, com destaque para tudo que ocorre em torno do Complexo Industrial e Portuário do Pecém.<br><br>

            O Jornal Porto do Pecém tem como objetivo oferecer um trabalho jornalístico sobre todos os movimentos mercadológicos do Brasil, especialmente do Ceará onde está localizado o Porto do Pecém.<br><br>

            Para obter a atenção de empresários, políticos, dirigentes governamentais, gestores, investidores, executivos, analistas de mercado, pretende-se que o Jornal Porto do Pecém seja um referencial para os órgãos de grande imprensa nacional. Destaque-se que nosso trabalho será em torno do que ocorre com as movimentações do 2º maior porto do Brasil.<br><br>

            É missão do Jornal Porto do Pecém relacionar, em caráter de assessoria - sempre em uma linha de claro serviço de utilidade ao leitor -, todos os segmentos econômicos e financeiros, nacionais e internacionais que possam estar direta ou indiretamente relacionadas com as múltiplas áreas da economia e das finanças regionais, principalmente no campo de importação e exportação.<br> <br> 

            Por fim, transmitir através de uma informação jornalística de primeiro plano, novos mercados, novas oportunidades de negócios e o conhecimento real das potencialidades do Complexo Industrial Porto do Pecém.
